                  <FormItem
                    className="flex flex-row 
                       items-end space-x-3 rounded-md border p-4
                       "
                  >
                    <FormControl>
                      <Checkbox
                        checked={field.value}
                        onCheckedChange={field.onChange}
                      />
                    </FormControl>
                    <FormLabel>Forest View</FormLabel>
                  </FormItem>
